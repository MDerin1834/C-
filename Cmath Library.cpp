#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    cout << cos(34)<<endl;
    cout << tan(34) << endl; 
    cout << exp(4) << endl; 
    cout << log(34) << endl;
    cout << log10(34) << endl;
    cout << pow(4,2) << endl;
    cout << sqrt(34) << endl;
    cout << cbrt(34) << endl;
    cout << round(34.8) << endl;
    cout << ceil(34.2) << endl;
    cout << floor(34.9) << endl;
    cout << fmin(34,44) << endl;
    cout << fdim(34,77) << endl;

}

